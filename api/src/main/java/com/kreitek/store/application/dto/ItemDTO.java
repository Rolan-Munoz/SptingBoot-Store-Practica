package com.kreitek.store.application.dto;

import java.io.Serializable;

public class ItemDTO implements Serializable {

    private Long id;
    private String name;
    private String description;
    private Double price;
    private byte[] image;
    // no queremos tener el objeto category entero solo alguna informacion de el
    private Long categoryId;
    private String categoryName;  // para estas dos propiedades hay que hacer cosas especiales en el mapper

    public ItemDTO() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public Double getPrice() {
        return price;
    }

    public void setPrice(Double price) {
        this.price = price;
    }

    public byte[] getImage() {
        return image;
    }

    public void setImage(byte[] image) {
        this.image = image;
    }

    public Long getCategoryId() {
        return categoryId;
    }

    public void setCategoryId(Long categoryId) {
        this.categoryId = categoryId;
    }

    public String getCategoryName() {
        return categoryName;
    }

    public void setCategoryName(String categoryName) {
        this.categoryName = categoryName;
    }
}
